                                      #列表的特点
'''·列表的特点
      ·列表元素按顺序有序排序
      ·索引映射唯一个数据
      ·列表可以存储重复数据
      ·任意数据类型混存
      ·根据需要动态分配和回收内存'''

lst1=['hello','world',98]   #列表中的元素通过索引获取
print(lst1)
print(lst1[0])
print(lst1[1])
print(lst1[-1])
print(lst1[-3])

lst1=['hello','world',98,'hello']   #列表可以存储重复数据
print(lst1[-1])
print(lst1[-4])