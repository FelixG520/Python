'''什么是字典
      ·字典
         ·python内置的数据结构之一，与列表一样是一个可变序列
         ·以键值对的方式存储数据，字典是一个无序的序列


      ·字典的实现原理
         ·字典的实现原理与查字典类似,查字典是先根据部首或拼音查找相应的页码（相当于哈希函数计算的过程），
         python中的字典是根据key查找value所在的位置


·字典的特征
- 通过键从字典中获取指定的项，但不能通过索引来获取
- 字典是无序的，也被称为hash表 (散列表)
- 是Python中的可变序列
- 字典中的键必须唯一，如果出现两次，后出现的将覆盖先出现的
- 字典中的键要求是不可变序列

         类似C语言枚举类型
         '''
